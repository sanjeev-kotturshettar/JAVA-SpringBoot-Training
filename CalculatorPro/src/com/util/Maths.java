package com.util;

public class Maths {
	//This method performs adding two integers
	public int add(int a, int b) {
		int result = a+b;
		return result;
	}
	
	//This method performs subtract two integers
	public int subtract(int a, int b) {
		int result = a-b;
		return result;
	}
	
	//This method performs multiply two integers
	public int multiply(int a, int b) {
		int result = a*b;
		return result;
	}
	
	//This method performs divide two integers
	public int divide(int a, int b) {
		int result = a/b;
		return result;
	}

}
